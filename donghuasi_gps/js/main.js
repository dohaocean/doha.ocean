    function playAudio() {
        var player = $("#mp3_audio")[0]; //将jquery对象转换成js对象
        if (audioState == true) {
            //console.log('1111');
            player.pause();
            $("#play_img").attr('src', 'images/played_bg.png');
            audioState = false;
        } else {
            //console.log('2222');
            player.src = now_mp3;
            player.load();
            player.play();
            $("#play_img").attr('src', 'images/playing_bg.gif');
            player.onended = function() {
                $("#play_img").attr('src', 'images/played_bg.png');
                audioState = false;
            }
            audioState = true;
        }
    }
    //播放
    $("#mapcon_modal").on("click", "#play_img", function() {
        //console.log('0000');
        playAudio();
    });

    //重置播放
    function resetAudio() {
        now_mp3 = '';
        audioState = false;
        var playBtn = $("#mp3_audio")[0]; //将jquery对象转换成js对象
        //console.log(playBtn,'9999');
        playBtn.pause();
        $("#play_img").attr('src', 'images/played_bg.png');
        
    }